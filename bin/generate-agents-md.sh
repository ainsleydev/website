#!/bin/bash

# generate-agents-md.sh
# Combines docs/AGENTS.md with filtered guidelines into root AGENTS.md
#
# This script generates comprehensive documentation for AI agents by:
# 1. Starting with project-specific docs from docs/AGENTS.md
# 2. Adding filtered guidelines from content/guidelines/ (go, js, html, scss, git)
# 3. Stripping Hugo frontmatter from all markdown files
# 4. Adjusting heading levels for consistent hierarchy
# 5. Outputting a single AGENTS.md file at the repository root
#
# Usage:
#   ./bin/generate-agents-md.sh
#   npm run agents
#
# Output: AGENTS.md (tracked in git)

set -e

# Output file
OUTPUT="AGENTS.md"

# Directories
DOCS_DIR="docs"
GUIDELINES_DIR="sites/ainsley-dev/content/guidelines"

# Technologies to include
TECHNOLOGIES=("go" "js" "html" "scss" "git")

echo "Generating $OUTPUT..."

# Add generated file warning header
cat > "$OUTPUT" << 'EOF'
<!--
  ⚠️  AUTO-GENERATED FILE - DO NOT EDIT DIRECTLY

  This file is automatically generated by bin/generate-agents-md.sh
  To update this documentation:
  1. Edit docs/AGENTS.md for project-specific content
  2. Edit sites/ainsley-dev/content/guidelines/ for coding standards
  3. Run: npm run agents
-->

EOF

# Add docs/AGENTS.md content (skip the note blockquote)
if [ -f "$DOCS_DIR/AGENTS.md" ]; then
    awk '
    BEGIN { in_note = 0 }
    /^> \*\*Note\*\*:/ { in_note = 1; next }
    in_note && /^>/ { next }
    in_note && /^$/ { in_note = 0; next }
    !in_note { print }
    ' "$DOCS_DIR/AGENTS.md" >> "$OUTPUT"
    echo "" >> "$OUTPUT"
else
    echo "Error: $DOCS_DIR/AGENTS.md not found"
    exit 1
fi

# Add guidelines section
echo "---" >> "$OUTPUT"
echo "" >> "$OUTPUT"
echo "# Developer Guidelines" >> "$OUTPUT"
echo "" >> "$OUTPUT"

# Process each technology
for tech in "${TECHNOLOGIES[@]}"; do
    tech_dir="$GUIDELINES_DIR/$tech"

    if [ ! -d "$tech_dir" ]; then
        echo "Warning: Directory $tech_dir not found, skipping..."
        continue
    fi

    echo "Processing $tech guidelines..."

    # Add technology section header
    echo "## ${tech^^} Guidelines" >> "$OUTPUT"
    echo "" >> "$OUTPUT"

    # Process _index.md if exists
    if [ -f "$tech_dir/_index.md" ]; then
        awk '/^---$/{if(++count==2){flag=1;next}}flag' "$tech_dir/_index.md" >> "$OUTPUT"
        echo "" >> "$OUTPUT"
    fi

    # Process all other .md files in the directory
    for file in "$tech_dir"/*.md; do
        if [ -f "$file" ] && [ "$(basename "$file")" != "_index.md" ]; then
            filename=$(basename "$file" .md)

            # Add subsection header
            echo "### ${filename^}" >> "$OUTPUT"
            echo "" >> "$OUTPUT"

            # Strip frontmatter and demote headings (## becomes ####, ### becomes ####)
            awk '
            /^---$/{if(++count==2){flag=1;next}}
            flag {
                # Trim leading whitespace for heading detection
                line = $0
                gsub(/^[[:space:]]+/, "", line)

                if (line ~ /^### /) {
                    sub(/^[[:space:]]*### /, "#### ", $0)
                } else if (line ~ /^## /) {
                    sub(/^[[:space:]]*## /, "#### ", $0)
                }
                print
            }' "$file" >> "$OUTPUT"
            echo "" >> "$OUTPUT"
        fi
    done
done

echo "✓ Generated $OUTPUT successfully!"
echo "  - Combined docs/AGENTS.md with guidelines"
echo "  - Included: ${TECHNOLOGIES[*]}"
